package csmath;

public class Mathpow {
    public static void main(String[] args) {
        System.out.println(Math.pow(0,1));
        System.out.println(Math.pow(1,1));
        System.out.println(Math.pow(1,2));
        System.out.println(Math.pow(10,2));
        System.out.println(Math.pow(5,0)); // anything power zero is 1

    }
}
